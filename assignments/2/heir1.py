import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
from scipy.cluster.hierarchy import dendrogram, linkage
from scipy.spatial.distance import pdist

def main():

    df = pd.read_feather('../../data/external/word-embeddings.feather')
    X = np.vstack(df['vit'].values)
    linkage_methods = ['single', 'complete', 'average']
    distance_metrics = ['euclidean', 'cosine', 'cityblock']

    for method in linkage_methods:
        for metric in distance_metrics:
            Z = linkage(pdist(X, metric=metric), method=method) # linkage matrix
            print(f"{method} linkage, {metric} distance")
            print(f"Linkage Matrix\n: {Z}")
            
            plt.figure(figsize=(10, 5))
            dendrogram(Z)
            plt.title(f"Dendrogram ({method} linkage, {metric} distance)")
            plt.savefig(f"figures/dendrogram({method} linkage, {metric} distance).png")
            # plt.show()

if __name__ == "__main__":
    main()


